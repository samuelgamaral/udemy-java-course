package c1fundamentos.operadores;

public class Atribuicao 
{
	public static void main(String[] args) {
		
		var a = 6;
		var b = 2;
		
		a += b;
		System.out.println(a);
		a -= b;
		System.out.println(a);
		a *= b;
		System.out.println(a);
		a /= b;
		System.out.println(a);
		a %= b;
		System.out.println(a);
		
		var x = "Teste de escrita da fonte Melon! 000";
		System.out.println(x);
	}
}